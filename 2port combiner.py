import numpy as np
import pandas as pd
import matplotlib.pyplot as plt
import joblib
import os

# ___________Loading the model
# Dataset is loaded only for plotting — not required for prediction itself.
# Used to show the training curve alongside the predicted point.

MODEL_PATH   = "model/rf_model.pkl"
DATASET_PATH = "2_port_augmented_dataset_1000.csv"

if not os.path.exists(MODEL_PATH):
    print("=" * 50)
    print("   Model not found!")
    print("  Please run 'train.py' first.")
    print("=" * 50)
    exit()

model = joblib.load(MODEL_PATH)
print("=" * 50)
print("   Model loaded from 'model/poly_ridge_model.pkl'")
print("=" * 50)

# ___________Load training dataset for plotting
df_train = None
if os.path.exists(DATASET_PATH):
    df_train = pd.read_csv(DATASET_PATH)
    df_train.columns = df_train.columns.str.strip()

# ___________Trained r2 range (from dataset)
R2_MIN = 0.02
R2_MAX = 0.30

# ___________Prediction function
def predict_port(r2_val):
    """Run model prediction for a single r2 value and display result."""
    user_X     = np.array([[r2_val]])
    prediction = model.predict(user_X)[0]

    print("\n" + "=" * 50)
    print("         PORT PREDICTION RESULTS")
    print("=" * 50)
    print(f"  Input r2       : {r2_val}")
    print("-" * 50)
    print(f"  {'Port':<12} {'Predicted Value':>20}")
    print("-" * 50)
    print(f"  {'Port 1':<12} {prediction:>20.6f}")
    print("=" * 50)
    return prediction

# ___________Plotting the predicted result
def plot_prediction(r2_val, prediction):
    """Plot predicted value against training data curve."""
    fig, ax = plt.subplots(figsize=(10, 5))
    fig.suptitle(f"User Prediction  |  r2 = {r2_val}", fontsize=13, fontweight='bold')

    if df_train is not None:
        df_sorted = df_train.sort_values('r2')
        ax.plot(df_sorted['r2'].values, df_sorted['Port 1'].values,
                color='steelblue', alpha=0.6, linewidth=1.5,
                label='Training data')

        # Green vertical line at the input r2
        ax.axvline(r2_val, color='green', linewidth=1.5,
                    linestyle=':', label=f'r2 = {r2_val}')

    # Red horizontal dashed line at the predicted value level
    ax.axhline(prediction, color='red', linewidth=1.5,
               linestyle='--', alpha=0.5, label=f'Predicted: {prediction:.4f}')

    # Red dot at the exact predicted point (intersection)
    ax.plot(r2_val, prediction,
            'o',
            color='red',
            markersize=10,
            zorder=5,
            label=f'▶ Point: ({r2_val}, {prediction:.4f})')

    # Annotate the dot with its value
    ax.annotate(f'  {prediction:.4f}',
                xy=(r2_val, prediction),
                fontsize=9,
                color='red',
                fontweight='bold',
                va='bottom')

    ax.set_title("Port 1 — Actual vs Predicted Point")
    ax.set_xlabel("r2")
    ax.set_ylabel("Port 1 Value")
    ax.legend(fontsize=9)
    ax.grid(True, alpha=0.4)

    plt.tight_layout()
    os.makedirs("plots", exist_ok=True)
    save_path = "plots/user_prediction.png"
    plt.savefig(save_path, dpi=150)
    plt.show()
    plt.pause(0.1) # Brief pause to allow Colab to render
    print(f"   Plot saved to '{save_path}'")


# ___________Batch prediction from CSV
def predict_from_csv(csv_path):
    """Batch predict from a CSV file containing an r2 column."""
    if not os.path.exists(csv_path):
        print(f"   File not found: {csv_path}")
        return

    batch_df = pd.read_csv(csv_path)
    batch_df.columns = batch_df.columns.str.strip()

    if 'r2' not in batch_df.columns:
        print("   CSV must contain an 'r2' column.")
        return

    out_of_range = batch_df[
        (batch_df['r2'] < R2_MIN) | (batch_df['r2'] > R2_MAX)
    ]
    if len(out_of_range) > 0:
        print(f"\n   Warning: {len(out_of_range)} rows have r2 outside"
              f" trained range [{R2_MIN} – {R2_MAX}]. Predictions may be inaccurate.")

    X_batch = batch_df[['r2']].values
    preds   = model.predict(X_batch)

    output_df = batch_df[['r2']].copy()
    output_df['port1_pred'] = preds

    save_path = "model/batch_predictions.csv"
    output_df.to_csv(save_path, index=False)

    print(f"\n   Batch predictions saved to '{save_path}'")
    print(output_df.to_string(index=False))


# ___________Main menu
while True:
    print("\n  Options:")
    print("    [1] Single prediction  (enter r2 manually)")
    print("    [2] Batch prediction   (load a CSV file)")
    print("    [3] Exit")

    choice = input("\nSelect option (1 / 2 / 3): ").strip()

    if choice == '1':
        while True:
            r2_input = input("\n  Enter r2 value (or 'back' to return): ").strip()
            if r2_input.lower() == 'back':
                break
            try:
                r2_val = float(r2_input)
            except ValueError:
                print("  Invalid value. Please enter a number.")
                continue

            if not (R2_MIN <= r2_val <= R2_MAX):
                print(f"\n  Warning: r2={r2_val} is outside the trained range"
                      f" [{R2_MIN} – {R2_MAX}]. Prediction may be inaccurate.")

            prediction = predict_port(r2_val)

            show_plot = input("\n  Show prediction plot? (y/n): ").strip().lower()
            if show_plot == 'y':
                plot_prediction(r2_val, prediction)
                plt.close('all') # Clean up plot memory

            again = input("\n  Predict again? (y/n): ").strip().lower()
            if again != 'y':
                break

    elif choice == '2':
        csv_path = input("\n  Enter path to CSV file: ").strip()
        predict_from_csv(csv_path)

    elif choice == '3':
        print("\n  Goodbye!")
        break

    else:
        print("  Invalid choice. Please enter 1, 2, or 3.")

