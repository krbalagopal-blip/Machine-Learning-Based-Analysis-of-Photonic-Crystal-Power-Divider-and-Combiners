model = SVR(
    kernel='rbf',    # kernel: 'rbf' | 'linear' | 'poly' | 'sigmoid'
    C=100,           # regularization: higher = less margin, fits data closer
    epsilon=0.01,    # tolerance tube around prediction; smaller = stricter fit
    gamma='scale'    # kernel coefficient: 'scale' | 'auto' | float
)
model.fit(X_train_scaled, y_train_scaled)
print("   Training complete.")

# _ Evaluating the Model
y_pred_scaled = model.predict(X_test_scaled)
y_pred = scaler_y.inverse_transform(y_pred_scaled.reshape(-1, 1)).ravel()

mse      = mean_squared_error(y_test, y_pred)
rmse     = np.sqrt(mse)
